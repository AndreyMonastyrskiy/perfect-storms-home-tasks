package module02.FINAL_TASK.ChessPieces;

/**
 * Enumeration of the types of pieces on the chessboard.
 * NONE type is for a virtual chess piece defining an empty square on a chessboard.
 *
 * @author Andrey Monastyrskiy
 */

public enum ChessPieceTypes {
    KING,
    ROOK,
    BISHOP,
    QUEEN,
    KNIGHT,
    PAWN,
    NONE;
}
